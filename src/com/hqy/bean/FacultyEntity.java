public class FacultyEntity {
    String   faculty_name;
    String   faculty_addr;
    String   faculty_contact;
    String   faculty_tel;
    /**
     * @return the faculty_name
     */
    public String getFaculty_name() {
        return faculty_name;
    }
    /**
     * @param faculty_name the faculty_name to set
     */
    public void setFaculty_name(String faculty_name) {
        this.faculty_name = faculty_name;
    }
    /**
     * @return the faculty_addr
     */
    public String getFaculty_addr() {
        return faculty_addr;
    }
    /**
     * @param faculty_addr the faculty_addr to set
     */
    public void setFaculty_addr(String faculty_addr) {
        this.faculty_addr = faculty_addr;
    }
    /**
     * @return the faculty_contact
     */
    public String getFaculty_contact() {
        return faculty_contact;
    }
    /**
     * @param faculty_contact the faculty_contact to set
     */
    public void setFaculty_contact(String faculty_contact) {
        this.faculty_contact = faculty_contact;
    }
    /**
     * @return the faculty_tel
     */
    public String getFaculty_tel() {
        return faculty_tel;
    }
    /**
     * @param faculty_tel the faculty_tel to set
     */
    public void setFaculty_tel(String faculty_tel) {
        this.faculty_tel = faculty_tel;
    }

}
